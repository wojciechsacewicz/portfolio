let lang = localStorage.getItem('lang') || 'en';
let theme = 'dark';

const langToggle = document.getElementById('lang-toggle');
const themeBtn = document.querySelector('.theme-btn');
const themeIcon = document.querySelector('.theme-icon');
const translatables = document.querySelectorAll('[data-translate]');

function updateLangButton() {
  if (langToggle) {
    langToggle.textContent = `lang=${lang}`;
  }
}

function applyLang(newLang) {
  if (!translations[newLang]) return;
  lang = newLang;
  localStorage.setItem('lang', lang);
  document.documentElement.setAttribute('lang', lang);

  translatables.forEach(el => {
    const key = el.dataset.translate;
    const value = translations[lang].ui[key];
    if (value) el.textContent = value;
  });

  updateExpandPills();
  updateLangButton();
}

function toggleLanguage() {
  applyLang(lang === 'en' ? 'pl' : 'en');
}

function initTheme() {
  const saved = localStorage.getItem('theme');
  if (saved === 'light') theme = 'light';
  document.body.classList.toggle('light-mode', theme === 'light');
  if (themeIcon) themeIcon.textContent = theme === 'dark' ? 'light' : 'dark';
  if (themeBtn) themeBtn.dataset.theme = theme === 'dark' ? 'light' : 'dark';
}

function toggleTheme() {
  theme = theme === 'dark' ? 'light' : 'dark';
  document.body.classList.toggle('light-mode', theme === 'light');
  if (themeIcon) themeIcon.textContent = theme === 'dark' ? 'light' : 'dark';
  if (themeBtn) themeBtn.dataset.theme = theme === 'dark' ? 'light' : 'dark';
  localStorage.setItem('theme', theme);
}

function initReveal() {
  const revealEls = document.querySelectorAll('.reveal');
  if (!revealEls.length) return;

  if (!('IntersectionObserver' in window)) {
    revealEls.forEach(el => el.classList.add('is-visible'));
    return;
  }

  const observer = new IntersectionObserver(
    entries => {
      entries.forEach(entry => {
        if (entry.isIntersecting) {
          entry.target.classList.add('is-visible');
          observer.unobserve(entry.target);
        }
      });
    },
    { threshold: 0.18, rootMargin: '0px 0px -8% 0px' }
  );

  revealEls.forEach(el => observer.observe(el));
}

function initImageFadeIn() {
  const images = document.querySelectorAll('.gallery-item img');
  images.forEach(img => {
    const show = () => img.classList.add('fade-in');
    if (img.complete) {
      show();
    } else {
      img.addEventListener('load', show, { once: true });
    }
  });
}

function initVideoPreview() {
  const videos = document.querySelectorAll('.work-panel video');
  videos.forEach(video => {
    video.play().catch(() => {
      video.controls = false;
    });
  });
}

function initLightbox() {
  const galleryItems = document.querySelectorAll('.gallery-item img');
  if (!galleryItems.length) return;

  const lightbox = document.createElement('div');
  lightbox.className = 'lightbox';
  lightbox.setAttribute('role', 'dialog');
  lightbox.setAttribute('aria-modal', 'true');
  lightbox.setAttribute('aria-label', 'Image preview');

  const img = document.createElement('img');
  const closeBtn = document.createElement('button');
  closeBtn.className = 'lightbox-close';
  closeBtn.type = 'button';
  closeBtn.textContent = 'x';
  closeBtn.ariaLabel = 'Close lightbox';

  lightbox.appendChild(img);
  lightbox.appendChild(closeBtn);
  document.body.appendChild(lightbox);

  const closeLightbox = () => {
    lightbox.classList.remove('active');
    setTimeout(() => {
      img.src = '';
      img.alt = '';
    }, 220);
  };

  galleryItems.forEach(item => {
    item.style.cursor = 'zoom-in';
    item.addEventListener('click', () => {
      img.src = item.src;
      img.alt = item.alt;
      lightbox.classList.add('active');
      closeBtn.focus();
    });
  });

  closeBtn.addEventListener('click', closeLightbox);
  lightbox.addEventListener('click', event => {
    if (event.target === lightbox) closeLightbox();
  });
  document.addEventListener('keydown', event => {
    if (event.key === 'Escape' && lightbox.classList.contains('active')) {
      closeLightbox();
    }
  });
}

function updateExpandPills() {
  document.querySelectorAll('[data-expand-pill]').forEach(pill => {
    const details = pill.closest('details');
    const isOpen = details ? details.open : false;
    const key = isOpen ? 'closeSection' : 'openSection';
    pill.textContent = translations[lang]?.ui?.[key] || (isOpen ? 'Close' : 'Open');
    pill.dataset.state = isOpen ? 'open' : 'closed';
  });
}

function initExpandableDetails() {
  const detailsList = document.querySelectorAll('details');
  if (!detailsList.length) return;

  const setOpenState = (details, open, animate = true) => {
    const content = details.querySelector('.expand-content');
    if (!content) return;

    const openContent = () => {
      content.style.maxHeight = `${content.scrollHeight}px`;
      content.style.opacity = '1';
      content.style.transform = 'translateY(0)';
    };

    const closeContent = () => {
      content.style.maxHeight = '0px';
      content.style.opacity = '0';
      content.style.transform = 'translateY(-8px)';
    };

    if (!animate) {
      content.style.transition = 'none';
      if (open) {
        details.open = true;
        openContent();
      } else {
        closeContent();
        details.open = false;
      }
      requestAnimationFrame(() => {
        content.style.transition = '';
        updateExpandPills();
      });
      return;
    }

    if (open) {
      details.open = true;
      requestAnimationFrame(() => {
        openContent();
        updateExpandPills();
      });
      return;
    }

    const onEnd = event => {
      if (event.propertyName !== 'max-height') return;
      content.removeEventListener('transitionend', onEnd);
      details.open = false;
      updateExpandPills();
    };
    content.addEventListener('transitionend', onEnd);
    requestAnimationFrame(() => {
      content.style.maxHeight = `${content.scrollHeight}px`;
      requestAnimationFrame(closeContent);
    });
  };

  detailsList.forEach(details => {
    setOpenState(details, details.open, false);
    const summary = details.querySelector('summary');
    if (!summary) return;

    summary.addEventListener('click', event => {
      event.preventDefault();
      setOpenState(details, !details.open, true);
    });
  });

  const openDetailsForHash = () => {
    const hash = window.location.hash;
    if (!hash) return;
    const target = document.querySelector(hash);
    if (!target) return;
    const details = target.closest('details');
    if (details) setOpenState(details, true, false);
    setTimeout(() => {
      target.scrollIntoView({ behavior: 'smooth', block: 'start' });
    }, 50);
  };

  openDetailsForHash();
  window.addEventListener('hashchange', openDetailsForHash);
}

function init() {
  initTheme();
  applyLang(lang);
  initReveal();
  initImageFadeIn();
  initVideoPreview();
  initLightbox();
  initExpandableDetails();
}

if (document.readyState === 'loading') {
  document.addEventListener('DOMContentLoaded', init);
} else {
  init();
}

if (langToggle) langToggle.addEventListener('click', toggleLanguage);
if (themeBtn) themeBtn.addEventListener('click', toggleTheme);
